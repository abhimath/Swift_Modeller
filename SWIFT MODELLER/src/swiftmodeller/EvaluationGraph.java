    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        if(chkfn == 0)
        {
            notrama = 0;
            new PsiPhi(cmod, this);
            new Ramachandran(cmod.substring(0, cmod.indexOf(".")) + ".psiphi");
            new RamaForm().setVisible(true);
        }
        else if(chkfn == 1)
        {
            notrama = 1;
            start = Integer.valueOf(jTextField1.getText());
            end = Integer.valueOf(jTextField2.getText());
            if(end <= start)
            {
                int rVal = jOptionPane1.showConfirmDialog(this, "Ending residue number is less than or equal to the starting residue number", null, -1, 3);
                if(rVal == jOptionPane1.OK_OPTION)
                {
                    jOptionPane1.setVisible(false);
                }
            }
            else
            {
                new LoopRefine(cmod, jTextField1.getText(), jTextField2.getText(), jTextField3.getText(), this.getComponent(0));
                new FlushScript(this);
                new SelectLoopModel().setVisible(true);
                this.setCursor(new Cursor(0));                
            }            
        }
        this.setVisible(false);
    }//GEN-LAST:event_jButton2ActionPerformed

