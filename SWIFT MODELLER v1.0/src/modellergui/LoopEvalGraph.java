    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        StringBuffer path = new StringBuffer(userdir);
        path.delete(path.indexOf(sep), path.length());
        StringBuffer root = new StringBuffer(path);
        path.append(sep + "launchpdb.bat");

        file = new File(path.toString());
        StringBuffer contents = new StringBuffer();
        BufferedWriter writer = null;

        try
        {
            contents.append("copy \"" + workpath + sep + "Jmol.jar" + "\" \"" + root + "\"\n");
            contents.append("copy \"" + workpath + sep + cmod + "\" \"" + root + "\"\n");
            contents.append("java -jar " + root + sep + "Jmol.jar " + cmod + "\n");
            contents.append("exit");
            writer = new BufferedWriter(new FileWriter(file));
            writer.write(contents.toString());
            writer.close();

            StringBuffer command = new StringBuffer("cmd /C start ");
            command.append(path);
            Runtime rt = Runtime.getRuntime();
            Process pr = rt.exec(command.toString());
        }
        catch (FileNotFoundException e)
        {
            new ErrorClose(this, true, "File Not Found : " + file.getName());
        }
        catch (IOException e)
        {
            new ErrorClose(this, true, "I/O Error");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

